import random
import datetime

def handle_logic(text):
    if not text: return None
    cmd = text.lower().strip()

    # --- فخر الذبحاني ---
    if cmd in [".الذبحاني", ".ذبحان"]:
        return "👑 **الذبحاني**.. أصل الكرم والوفاء، رجال الشدائد ومنبع العز. والنعم بـ سعيد وكل أهل ذبحان."

    # --- خدمات زيادة المتابعين والدعم ---
    elif cmd == ".خدمات":
        return (
            "🚀 **خدمات سعيد ثون (الذبحاني) للزيادة**\n"
            "━━━━━━━━━━━━━━━━━━\n"
            "✅ زيادة متابعين إنستقرام وتيك توك (حقيقي)\n"
            "✅ زيادة مشاهدات ريلز ولايكات فورية\n"
            "✅ دعم قنوات تليجرام وقروبات واتساب\n"
            "✅ بيع حسابات ويوزرات مميزة\n"
            "━━━━━━━━━━━━━━━━━━\n"
            "💬 اطلب الآن عبر الخاص أو أرسل: **.مطور**"
        )

    # --- محتوى بدوي وخواطر (شعر الذبحاني) ---
    elif cmd in [".شعر", ".بدوي", ".بوح"]:
        ashar = [
            "حنّا بدو.. فينا كرامة وفينا طيب.. وفينا على الشدات صبر وعزيمة. 🐪",
            "يا بوي انا ماني بمخلوق من طين.. انا من العز والكرامة مكوّن. 👑",
            "الذيب ما يهرول عبث، والحر تكفيه الإشارة.. والرفيق الكفو ذخر في الليالي العسيرة. ✨",
            "ما يكسر الذبحاني الوقت لو جار.. حنّا جبالٍ ما تهزها العواصف. 🌋"
        ]
        return f"📜 **{random.choice(ashar)}**"

    elif cmd in [".خواطر", ".عبرة"]:
        ebr = [
            "💡 خلك مثل الجبل، مهما اشتدت الريح تبقى ثابت.",
            "💡 الصمت في بعض المواقف هيبة، والرد في وقته حكمة.",
            "💡 الزمن مدرسة، والذكي هو اللي يتعلم من دروسه."
        ]
        return f"✨ {random.choice(ebr)}"

    # --- الذهب والفوركس ---
    elif cmd == ".الذهب":
        return "📊 **مراقب الذهب (XAUUSD):**\nالسوق تحت المراقبة يا ذبحاني.. خلك حذر مع التداولات اليوم."

    # --- الأوامر الأساسية ---
    elif cmd == ".فحص":
        return "✅ **سعيد ثون [Saeedthon]**\nالبوت شغال بأعلى كفاءة لعيون الذبحاني! 🚀"

    elif cmd == ".الوقت":
        # عرض الوقت بتوقيت صنعاء كما تحب
        now = datetime.datetime.now().strftime("%I:%M %p")
        return f"⏰ الوقت الحالي في صنعاء: **{now}**"

    elif cmd == ".مطور":
        return (
            "👑 **مطور النظام:** سعيد حسن الذبحاني\n"
            "📍 صنعاء - اليمن\n"
            "📱 بوت المنصات الشامل (Saeedthon)."
        )

    return None
