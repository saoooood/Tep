import datetime

def handle_logic(text):
    """
    هذه الدالة تستقبل أي رسالة من أي منصة (تيليجرام، انستا، واتساب)
    وتقرر ما هو الرد المناسب.
    """
    if not text:
        return None

    # تنظيف النص وتحويله للصغير للمقارنة
    cmd = text.lower().strip()

    # --- قسم الأوامر العامة ---
    
    if cmd == ".فحص":
        return "✅ سعيد ثون [Saeedthon] يعمل بنجاح على هذه المنصة!"

    elif cmd == ".اوامري":
        return (
            "👑 قائمة أوامر سعيد ثون:\n\n"
            "• `.فحص` : للتأكد من عمل البوت\n"
            "• `.الوقت` : لعرض الوقت الحالي\n"
            "• `.المطور` : معلومات المطور\n"
            "• `.الذهب` : حالة سوق الذهب (قريباً)"
        )

    elif cmd == ".الوقت":
        now = datetime.datetime.now().strftime("%I:%M %p")
        return f"⏰ الوقت الحالي بتوقيتك: {now}"

    elif cmd == ".المطور":
        return "👑 مطور هذا النظام هو: سعيد حسن الضحباني."

    # --- قسم خاص بالـ Forex والذهب (بما أنك مهتم بها) ---
    
    elif cmd == ".الذهب":
        # ملاحظة: يمكنك مستقبلاً ربط هذا الأمر بـ API لجلب الأسعار مباشرة
        return "📊 تحليل الذهب (XAUUSD):\nالسوق قيد المراقبة، يرجى الحذر من التقلبات."

    # إذا لم تكن الرسالة تبدأ بـ "." أو ليست أمراً، لا يرد البوت
    return None

